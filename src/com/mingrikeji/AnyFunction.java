package com.mingrikeji;
import java.math.*;;
public class AnyFunction {
	public static void main(String[] args) {
		System.out.println("4和8较大者:" + java.lang.Math.max(4, 8)); 
		 // 取两个参数的最小值
		System.out.println("4.4和4较小者：" + java.lang.Math.min(4.4, 4));
		System.out.println("-7的绝对值：" + java.lang.Math.abs(-7)); // 取参数的绝对值
	}
}
